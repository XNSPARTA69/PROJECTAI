const form = document.getElementById('upload-form');
const results = document.getElementById('results');
const description = document.getElementById('description');
const processedImage = document.getElementById('processed-image');
const rawOutput = document.getElementById('raw-output');
const downloadLink = document.getElementById('download-link');
const status = document.getElementById('status');

// When I submit the form, I intercept the event to handle the image upload and results display myself
form.addEventListener('submit', async (e) => {
    e.preventDefault();
    
    // I show the loading message and reset UI elements
    results.classList.remove('hidden');
    description.textContent = 'Processing image, please wait...';
    processedImage.classList.add('hidden');
    downloadLink.classList.add('hidden');
    rawOutput.textContent = '';
    status.textContent = '';
    status.className = 'status-message';

    const formData = new FormData(form);

    try {
        // I send the image to the backend for processing
        const response = await fetch('/process', {
            method: 'POST',
            body: formData
        });

        const data = await response.json();

        if (response.ok) {
            // I show the description generated by LLaMA
            description.textContent = data.description || 'No description was generated.';
            
            // If an output image is available, I show it and the download link
            if (data.output_image) {
                const imageUrl = `/${data.output_image}?t=${new Date().getTime()}`;
                processedImage.src = imageUrl;
                processedImage.classList.remove('hidden');
                downloadLink.href = imageUrl;
                downloadLink.classList.remove('hidden');
            } else {
                processedImage.classList.add('hidden');
                downloadLink.classList.add('hidden');
            }
            
            // I show the raw detection and segmentation data for debugging or advanced users
            rawOutput.textContent = JSON.stringify({
                detections: data.detections,
                segmentation: data.segmentation
            }, null, 2);
            
            // I display a success message
            status.textContent = 'Processing completed successfully.';
            status.className = 'status-message success';
        } else {
            // If there was an error from the backend, I display it
            description.textContent = `Error: ${data.error}`;
            processedImage.classList.add('hidden');
            downloadLink.classList.add('hidden');
            rawOutput.textContent = '';
            status.textContent = `Error: ${data.error}`;
            status.className = 'status-message error';
        }
    } catch (error) {
        // If there was a network error, I display it
        description.textContent = `Network error: ${error.message}`;
        processedImage.classList.add('hidden');
        downloadLink.classList.add('hidden');
        rawOutput.textContent = '';
        status.textContent = `Network error: ${error.message}`;
        status.className = 'status-message error';
    }
});